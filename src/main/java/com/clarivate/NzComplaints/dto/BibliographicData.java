package com.clarivate.NzComplaints.dto;

public class BibliographicData {

    private String applicationNumber;
    private String applicantName;
    private String applicantAddress;
    private String trademarkClass;
    private String markType;
    private String markName;
    private String redParty;
    private String firstActionType;
    private String firstActionDate;

    // Getters and Setters
    public String getApplicationNumber() {
        return applicationNumber;
    }

    public void setApplicationNumber(String applicationNumber) {
        this.applicationNumber = applicationNumber;
    }

    public String getApplicantName() {
        return applicantName;
    }

    public void setApplicantName(String applicantName) {
        this.applicantName = applicantName;
    }

    public String getApplicantAddress() {
        return applicantAddress;
    }

    public void setApplicantAddress(String applicantAddress) {
        this.applicantAddress = applicantAddress;
    }

    public String getTrademarkClass() {
        return trademarkClass;
    }

    public void setTrademarkClass(String trademarkClass) {
        this.trademarkClass = trademarkClass;
    }

    public String getMarkType() {
        return markType;
    }

    public void setMarkType(String markType) {
        this.markType = markType;
    }

    public String getMarkName() {
        return markName;
    }

    public void setMarkName(String markName) {
        this.markName = markName;
    }

    public String getRedParty() {
        return redParty;
    }

    public void setRedParty(String redParty) {
        this.redParty = redParty;
    }

    public String getFirstActionType() {
        return firstActionType;
    }

    public void setFirstActionType(String firstActionType) {
        this.firstActionType = firstActionType;
    }

    public String getFirstActionDate() {
        return firstActionDate;
    }

    public void setFirstActionDate(String firstActionDate) {
        this.firstActionDate = firstActionDate;
    }
}
